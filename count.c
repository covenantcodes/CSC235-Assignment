#include <stdio.h>
int main () {
    int i = 0;
    printf("Program to count from 1 to N; where N is equal to 10\n");
    for ( i = 0; i <= 10; i++)
    {
        printf("%d\n", i);
    }
    printf("End of Count \n");
    return 0 ;
}   